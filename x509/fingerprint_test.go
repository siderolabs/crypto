func TestSPKIFingerprints(t *testing.T) {
	fingerprint1, err := x509.SPKIFingerprintFromPEM([]byte(certPEM1))
	if err != nil {
		t.Fatal(err)
	}

	expected, err := x509.ParseFingerprint("pnduMqbUZ/sQyDW1o4kP6c1u1PoB2gRQu8KEV38guAM=")
	if err != nil {
		t.Fatal(err)
	}

	if !fingerprint1.Equal(expected) {
		t.Fatalf("expected %s != %s", expected, fingerprint1)
	}

	fingerprint2, err := x509.SPKIFingerprintFromPEM([]byte(certPEM2))
	if err != nil {
		t.Fatal(err)
	}

	if fingerprint1.Equal(fingerprint2) {
		t.Fatalf("shouldn't be equal %s %s", fingerprint2, fingerprint1)
	}

	block1, _ := pem.Decode([]byte(certPEM1))

	cert1, err := stdx509.ParseCertificate(block1.Bytes)
	if err != nil {
		t.Fatal(err)
	}

	block2, _ := pem.Decode([]byte(certPEM2))

	cert2, err := stdx509.ParseCertificate(block2.Bytes)
	if err != nil {
		t.Fatal(err)
	}

	err = x509.MatchSPKIFingerprints(fingerprint1)(tls.ConnectionState{
		PeerCertificates: []*stdx509.Certificate{cert1, cert2},
	})
	if err != nil {
		t.Fatal(err)
	}

	err = x509.MatchSPKIFingerprints(fingerprint1)(tls.ConnectionState{
		PeerCertificates: []*stdx509.Certificate{cert2, cert1},
	})
	if err == nil {
		t.Fatal("non-leaf certificate fingerprint should be rejected")
	}

	err = x509.MatchSPKIFingerprints(fingerprint1, fingerprint2)(tls.ConnectionState{
		PeerCertificates: []*stdx509.Certificate{cert2, cert1},
	})
	if err != nil {
		t.Fatal(err)
	}

	err = x509.MatchSPKIFingerprints(fingerprint1, fingerprint2)(tls.ConnectionState{})
	if err == nil {
		t.Fatal("connection without peer certificates should be rejected")
	}
}
